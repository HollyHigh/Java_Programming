public class Conversion {
    public static void main(String[] args) {
        // Implicit conversion
        int number1 = 10;
        double number2 = 10;
        System.out.println("number1: " + number1);
        System.out.println("number2: " + number2);


        // Explicit conversion

        int number3 = (int) 2.99;
//        int number4 = 2.99;
        System.out.println("number3: " + number3);
    }
}
